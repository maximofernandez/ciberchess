class Figura {

    Image imagen;
    int x, y, z, t;
    String nombre;

    public Figura(Image img, int a, int b, int c, int d, String nom) 
    {
      imagen = img;
      x = a;
      y = b;
      z = c;
      t = d;
      nombre = nom;
    }
}
